#include <iostream>

using namespace std;

struct node
{
    int data;
    node *next;
};

class linked_list
{
private:
    node *head,*tail;
public:
    linked_list()
    {
        head = NULL;
        tail = NULL;
    }
    void add_node(int n)
    {
        node *tmp = new node;
        tmp->data = n;
        tmp->next = NULL;

        if(head == NULL)
        {
            head = tmp;
            tail = tmp;
        }
        else
        {
            tail->next = tmp;
            tail = tail->next;
        }
    }
    void Printlist(struct node *ptr)
    {
         while(ptr != NULL)
         {
            std::cout<<"->"<<ptr->data;
            ptr = ptr->next;  
         }
         std::cout<<"NULL \n";
    }
};
